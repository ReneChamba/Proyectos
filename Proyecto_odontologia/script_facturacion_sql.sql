DROP TABLE IF EXISTS CUENTAS;
CREATE TABLE CUENTAS(
		CUE_COD INTEGER PRIMARY KEY AUTO_INCREMENT,
        CUE_RUC VARCHAR(13) NOT NULL, # CORRESPONDE A SU USUARIO
        CUE_PAS  VARCHAR(250) NOT NULL, # CONTRASEGNIA
        CUE_EST TINYINT NOT NULL, #ESTADO, ACTIVO O DESACTIVO
		CUE_FEC_REG DATE NOT NULL, #FECHA REGISTRO
        CUE_ROL VARCHAR(25) NOT NULL, # ROL
        CONSTRAINT UNIQUE_RUC_CUE  UNIQUE(CUE_RUC) # RESTRICCION PARA QUE NO EXISTAN RUC IGUALES
);

/*
Tabla emisores/remitentes
*/
DROP TABLE IF EXISTS EMISORES;
CREATE TABLE EMISORES(
				EMI_COD INTEGER PRIMARY KEY,
				EMI_RUC VARCHAR(15) NOT NULL,
                EMI_RAZ_SOC VARCHAR(250) NOT NULL, # RAZON SOCIAL/ NOMBRES APELLIDOS 
				EMI_NOM_COM VARCHAR(250), # NOMBRE COMERCIAL
				EMI_DIR_MAT VARCHAR(250), # DIRECCION ESTABLECIMIENTO MATRIZ
                EMI_COD_EST_EMI VARCHAR(3) NOT NULL, # CODIGO ESTABLECIMIENTO EMISOR
				EMI_DIR_EST VARCHAR(250), # DIRECCION ESTABLECIMIENTO EMISOR  
                EMI_COD_PTO_EMI	 VARCHAR(3) NOT NULL, #CODIGO PUNTO DE EMISION
				EMI_CON_ESP VARCHAR(5), # CONTRIBUYENTE ESPECIAL (NUMERO DE RESOLUCION)
				EMI_OBL_CON ENUM('SI', 'NO') NOT NULL , # OBLIGADO A LLEVAR CONTABILIDAD
                EMI_TIP ENUM('NATURAL', 'JURIDICA'), # TIPO DE PERSONA
				EMI_LOG VARCHAR(255), # LOGO 
                EMI_TIP_AMB VARCHAR(1) NOT NULL, # TIPO DE AMBIENTE
				EMI_TIP_EMI VARCHAR(1)NOT NULL, # TIPO DE EMISION
				EMI_REG_RIM TINYINT NOT NULL, # REGIMEN RIMPE
                EMI_PRO VARCHAR(2), #CODIGO DE LA PROVINCIA, ESTE ES NETAMENTE PARA EL SISTEMA
				CONSTRAINT UNIQUE_RUC  UNIQUE(EMI_RUC), # RESTRICCION PARA QUE NO EXISTAN DOS RUC IGUALES
				CONSTRAINT REL_EMI_CUE FOREIGN KEY(EMI_COD) REFERENCES CUENTAS(CUE_COD)
                );  
                
/*
	TABLE ESTABLECIMIENTOS
