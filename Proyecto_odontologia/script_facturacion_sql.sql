/*
TABLE  PUNTO DE EMISION
*/

DROP TABLE IF EXISTS PUNTOS_EMISION;

CREATE TABLE PUNTOS_EMISION(
    /*
    El punto de emision debe ser unico dentro de cada establecimiento, por ende se debe valdiar
    si cada nuevo punto no existe en el establecimiento actual
    */
   PTE_COD INTEGER PRIMARY KEY AUTO_INCREMENT,
    PTE_COD_EMI VARCHAR(4)  NOT NULL, # CODIGO DEL PUNTO DE EMISION
    PTE_EST CHAR(1) NOT NULL, # ESTADO DEL PUNTO EMISION
    PTE_FEC DATE,
    PTE_CLA VARCHAR(250), # CLAVE DEL PUNTO DE EMISION
    PTE_FK_COD_EST VARCHAR(4) NOT NULL,
     # PRIMARY KEY (PTE_COD_EMI, PTE_FK_COD_EST),
    CONSTRAINT UNIQUE_PTE_EST  UNIQUE(PTE_COD_EMI,PTE_FK_COD_EST), # RESTRICCION PARA QUE NO EXISTAN DOS CODIGO ESTABLECIMIENTOS Y PUNTO EMISION IGUALES
	CONSTRAINT REL_PTE_EST FOREIGN KEY(PTE_FK_COD_EST) REFERENCES  ESTABLECIMIENTOS(EST_COD)
    
);


