DROP TABLE IF EXISTS ESTABLECIMIENTOS;
CREATE TABLE ESTABLECIMIENTOS(
    EST_COD VARCHAR(4) PRIMARY KEY NOT NULL, # CODIGO DE TRES DIGITOS DEL ESTABLECIMIENTO,  EJM:001
    EST_RAZ_SOC VARCHAR(250), # NOMBRE SUCURSAL, DEBERIA SER EL MISMO QUE EL PRINCIPAL
